package basic;

import java.util.Scanner;

public class ForLoop {

	public static void main(String[] args) {
		
		Scanner sc=new Scanner(System.in);
		System.out.println("please enter the numbers");
		int n=sc.nextInt();
		for(int i=n;i<=n;i++) {
			
			
			for(int j=1;j<=10;j++) {
				
				   System.out.println(i+"*"+j+"="+(i*j));
				  
				
			}
			System.out.println("===========");
			
			
			
			
		}
		

		 
}
}
